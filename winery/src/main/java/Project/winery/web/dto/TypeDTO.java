package Project.winery.web.dto;

public class TypeDTO {
	
	private Long id;
	
	private String name;

	public TypeDTO() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
